package config;

public class ItemModel {
    String txtItem, txtAmount, inputQuantity, datePurchase, dropdownMonth, txtRemarks;

    public String getTxtItem() {
        return txtItem;
    }

    public void setTxtItem(String txtItem) {
        this.txtItem = txtItem;
    }

    public String getTxtAmount() {
        return txtAmount;
    }

    public void setTxtAmount(String txtAmount) {
        this.txtAmount = txtAmount;
    }

    public String getInputQuantity() {
        return inputQuantity;
    }

    public void setInputQuantity(String inputQuantity) {
        this.inputQuantity = inputQuantity;
    }

    public String getDatePurchase() {
        return datePurchase;
    }

    public void setDatePurchase(String datePurchase) {
        this.datePurchase = datePurchase;
    }

    public String getDropdownMonth() {
        return dropdownMonth;
    }

    public void setDropdownMonth(String dropdownMonth) {
        this.dropdownMonth = dropdownMonth;
    }

    public String getTxtRemarks() {
        return txtRemarks;
    }

    public void setTxtRemarks(String txtRemarks) {
        this.txtRemarks = txtRemarks;
    }
}
